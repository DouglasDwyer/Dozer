    /// <summary>
    /// Maps from type object to builtin ID.
    /// </summary>
    private static readonly ImmutableDictionary<Type, ushort> _typeToId = _idToType
            .Select((ty, index) => new KeyValuePair<Type, ushort>(ty, (ushort)index)).ToImmutableDictionary();

    /// <summary>
    /// Gets the object associated with the given ID, if any.
    /// </summary>
    /// <param name="id">The name hash of the object.</param>
    /// <param name="type">The object, if found, is written to this variable.</param>
    /// <returns><c>true</c> if an object was found for the ID.</returns>
    public static bool TryGetType(ushort id, [NotNullWhen(true)] out Type? type)
    {
        if (id < _idToType.Length)
        {
            type = _idToType[id];
            return true;
        }
        else
        {
            type = null;
            return false;
        }
    }

    /// <summary>
    /// Gets the ID associated with the given object, if any.
    /// </summary>
    /// <param name="type">The object in question.</param>
    /// <param name="id">The name hash, if found, is written to this variable.</param>
    /// <returns><c>true</c> if the object had an ID.</returns>
    public static bool TryGetId(Type type, out ushort id)
    {
        return _typeToId.TryGetValue(type, out id);
    }
